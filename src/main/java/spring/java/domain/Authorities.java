package spring.java.domain;

public class Authorities {
	private Long id;
	private String username;
	private String authority;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public String getAuthority() {
		return authority;
	}

}
